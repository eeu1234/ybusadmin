package com.test.spring.dto;

public class SignFileDTO {
	
	private String signFileSeq;
	private String signFileName;
	private String signFileType;
	public String getSignFileSeq() {
		return signFileSeq;
	}
	public void setSignFileSeq(String signFileSeq) {
		this.signFileSeq = signFileSeq;
	}
	public String getSignFileName() {
		return signFileName;
	}
	public void setSignFileName(String signFileName) {
		this.signFileName = signFileName;
	}
	public String getSignFileType() {
		return signFileType;
	}
	public void setSignFileType(String signFileType) {
		this.signFileType = signFileType;
	}

}
